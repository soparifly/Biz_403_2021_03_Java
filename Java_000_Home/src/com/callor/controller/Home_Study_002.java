package com.callor.controller;

public class Home_Study_002 {
	public static void main(String[] args) {
		int num1 = 0;
		int num2 = 0;

		int intSum = 0;
		int intMinus = 0;
		int intTimes = 0;
		int intDiv = 0;

		num1 = 33;
		num2 = 55;

		intSum = num1 + num2;
		intMinus = num2 - num1;
		intTimes = num1 * num2;
		intDiv = num2 / num1;

		System.out.println("33 + 55 = " + intSum);
		System.out.println("55 - 33 = " + intMinus);
		System.out.println("33 * 55 = " + intTimes);
		System.out.println("55 / 33 = " + intDiv);

	}
}
