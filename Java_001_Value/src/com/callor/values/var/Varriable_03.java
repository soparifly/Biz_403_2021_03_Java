package com.callor.values.var;

public class Varriable_03 {

	public static void main(String[] args) {
		//정수형(Integer Type) 변수 num1 선언
		//정수값을 저장할 기억장소를 예약하고 
		//한번 변수를 선언하는데 사용한 이름으로 또다시 변수를 선언할수없다.
		
		// num1  이름을 부여하기
		int num1;
		
		// 또다른 기억장소를 예약하고 num2 이름 부여
		int num2;
		
		
		
		//선언된 변수에 값을 저장하기
		 num1 = 30;
		 num2 = 40;
		 
		 
		 System.out.println(num1);
		 System.out.println(num2);
		 
		 //num1변수와 num2변수에 어떤 값에 저장되어있는지는 고정되지않는다.
		 // 모두 제거하고 num1에 150, num250을 저장하라
		 num1= 150;
		 num2= 250;
		 
		 System.out.println(num1);
		 System.out.println(num2);
		 
		 
	}

}
