package com.callor.values;

public class Values_02 {
	public static void main(String[] args) {
		
		System.out.println("1 .반지름이 23인 원의 둘레와 넓이를 계산하여 출력");
		
		System.out.println("============================================");
		
		System.out.println("   원의 둘레 : 반지름 * 2 * 3.14");
		
		System.out.print("        둘레 : ");
		System.out.println(23 * 2 * 3.14);
		
		System.out.println("   원의 넓이 : 반지름 * 반지름 * 3.14");
		
		System.out.print("        넓이 : ");
		System.out.println(23 * 23 * 3.14);
		
		System.out.println("===========================================");
	}
}
