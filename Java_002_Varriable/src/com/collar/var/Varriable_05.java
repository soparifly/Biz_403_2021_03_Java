package com.collar.var;

public class Varriable_05 {
	public static void main(String[] args) {
	
		int intNum = 1;
		int intSum = 0;
		
		intSum += intNum;  //1
				
		intNum ++;  //2
		intSum += intNum; // 1 + 2
		
		intNum ++; //4
		intSum += intNum; // 3 + 4
		
		intNum ++; //5
		intSum += intNum; // 7 + 5 
		
		
		
		
		
		
	}
}
