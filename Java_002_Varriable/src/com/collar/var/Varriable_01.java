package com.collar.var;

public class Varriable_01 {

	
	public static void main(String[] args) {
		
	
		
		int num1= 33;
			
		int num2= 55;

		
		
		
		
		System.out.println("==================================");
		
		System.out.printf(" %d + %d = %d\n",num1,num2,(num1+num2));
		System.out.printf(" %d - %d = %d\n",num1,num2,(num1-num2));
		System.out.printf(" %d * %d = %d\n",num1,num2,(num1*num2));
		System.out.printf(" %d / %d = %d\n",num1,num2,(num1/num2));
		
		System.out.println("==================================");
			
		
	}

}
